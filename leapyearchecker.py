def is_leap(year):
    if year % 4 == 0:
        if year % 100 == 0:
            if year % 400 == 0:
                return True
            else:
                return False
        else:
            return True
    else:
        return False


def days_in_month(year, month):
    month_days = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]
    get_num_of_days = month_days[month - 1]
    if month == 2:
        if is_leap(year):
            return 29
        else:
            return 28
    else:
        return get_num_of_days


